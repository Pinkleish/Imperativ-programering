# We can define our own functions with the
# def keyword.

# Here we define a function with the name is_odd
# The function takes one parameter, called x:

def är_udda(x):
    return x % 2 == 1

# Note the body of the function is indented.
# The return statement can be used to return
# a value.

# Defining the function does not evaluate it.
# We do that with the familiar syntax:
result = är_udda(-45)
print(result)






